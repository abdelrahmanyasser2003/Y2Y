class ProfileModel {
  final String name;
  final String phone;
  final String email;
  final String street;
  final String city;
  final String state;
  final String gender;

  ProfileModel({
    required this.name,
    required this.phone,
    required this.email,
    required this.street,
    required this.city,
    required this.state,
    required this.gender,
  });
}
